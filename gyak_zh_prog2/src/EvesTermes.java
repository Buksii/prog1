public class EvesTermes {


    int ev;
    double osszterm;
    int avgterm;

    public EvesTermes(int ev, int osszterm) {
        this.ev = ev;
        this.osszterm = osszterm;
    }

    public int getEv() {
        return ev;
    }

    public void setEv(int ev) {
        this.ev = ev;
    }

    public double getOsszterm() {
        return osszterm;
    }

    public void setOsszterm(int osszterm) {
        this.osszterm = osszterm;
    }

    public int getAvgterm() {
        return avgterm;
    }

    public void setAvgterm(int avgterm) {
        this.avgterm = avgterm;
    }
}
